import java.util.Scanner;

class Codechef
{
	public static void main (String[] args)
	{
        Scanner read = new Scanner(System.in);
        // N should be an int
        int N = read.nextInt();
        // Use next() to read the string
        String S = read.next();
        // Add a space " " between N and S
		System.out.print(N + " ");
		System.out.print(S);
	}
}